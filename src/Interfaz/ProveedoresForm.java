    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre para el proveedor");
            txtNombre.requestFocus();
            return;
        }
        if (txtTienda.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre de la tienda del proveedor'");
            txtTienda.requestFocus();
            return;
        }

        // SI PASO LAS VALIDACIONES
        String status;
        if (cbStatus.getSelectedIndex() == 0) {
            status = "OK";
        }else{
            status = "des";
        }
        
        ctrl.llenarModelo(txtNombre.getText(), txtTienda.getText(), status);
        
        if (ctrl.add()){
            JOptionPane.showMessageDialog(this, "Proveedor agregado correctamente");
            //mostrar("");
        } else{
            JOptionPane.showMessageDialog(this, "Proveedor NO agregado, intentalo m√°s tarde");
        }
        
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

